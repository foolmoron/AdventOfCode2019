function fft(input) {
    const pattern = [0,1,0,-1];
    for (let i = 0; i < input.length; i++) {
        let sum = 0;
        // let mults = []
        for (let ii = 0; ii < input.length; ii++) {
            let mult = pattern[(Math.floor((ii + 1) / (i + 1))) % 4];
            // mults.push(mult);
            sum += input[ii] * mult;
        }
        input[i] = Math.abs(sum) % 10;
        // console.log(i, JSON.stringify(mults));
    }
    return input;
}

